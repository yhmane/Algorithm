package ju;

import java.util.Scanner;


public class Ju10950 {

    public Ju10950() {}
    public void solution() {
    	Scanner sc = new Scanner(System.in);
    	int t = sc.nextInt();
    	for(int i = 1; i <= t; i ++) {
    		int a = sc.nextInt();
    		int b = sc.nextInt();
    		System.out.println(a + b);
    		sc.nextLine();
    	}
    	sc.close();
    }
}



